function greet(theTime) {
  let hour = theTime.split(":")[0];
  if (hour < 12) {
    return "Good Morning";
  } else if (hour < 18) {
    return "Good Afternoon";
  } else {
    return "Good Evening";
  }
}

function displayMessage(string) {
  let toUpdate = document.querySelector('#greeting');
  toUpdate.innerText = string; 
}
